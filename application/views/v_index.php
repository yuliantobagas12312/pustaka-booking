 <b><p>Pengertian CodeIgniter</p></b>
 <p align='justify'>CodeIgniter adalah sebuah framework berbasis PHP yang kuat dengan footprint yang sangat kecil, dibangun untuk pengembang yang membutuhkan toolkit sederhana dan elegan untuk membuat aplikasi web dengan fitur lengkap. Lantas Apa itu framework? Framework atau dalam bahasa indonesia dapat diartikan sebagai “kerangka kerja” merupakan sebuah toolyang bekerja pada suatu konsep tertentu dan terdiri dari berbagai fungsi yang dapat dengan mudah dijabarkan atau dialih-gunakan untuk membuat fungsi-fungsi lain yang lebih kompleks. Maksudnya, jika seorang Programmer menggunakan sebuah framework sebagai lingkup kerjanya, dia harus mematuhi segala ketentuan dari framework tersebut. Lebih lanjut ia dapat membangun berbagai fungsi yang rumit dan kompleks dengan menggunakan fungsi-fungsi yang telah disediakan oleh Framework tersebut. Jadi dia tidak perlu lagi menulis ulang semua coding, Ia cukup mempelajari cara menggunakan kerangka (frame) yang telah tersedia dari framework tersebut.</p>

 <p align='justify'>Sebagaimana yang telah saya utarakan sebelumnya bahwa CodeIgniter adalah sebuah kerangka kerja yang berisi berbagai fungsi yang dapat digunakan untuk membuat fungsi-fungsi yang kompleks. Dalam membangun sebuah web, kita biasa sangat berkutat pada script koneksi database, pagination, proses login, query database. Dengan menggunakan sebuah framework, CodeIgniter tentu saja, kita tidak perlu lagi menuliskan perintah mysql_connect, myql_select_db atau semacamnya, karena CodeIgniter sudah menyediakan semua itu. Kita hanya perlu tau bagaimana cara menggunakannya. Dan saya rasa, semua itu tidaklah terlalu susah. Jadi intinya adalah: ia mampu mempercepat dan mempermudah kita dalam pembuatan website.</p>
